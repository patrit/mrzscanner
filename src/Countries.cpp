bool Countries::has_country(string country) {
  country.erase(std::remove(country.begin(), country.end(), '<'),
                country.end());
  return countries.find(country) != countries.end();
}

void Countries::toJson(std::ostream &out) {
  static string code("code");
  static string en("en");
  Poco::JSON::Array ret;
  size_t idx = 0;
  for (auto const &[key, val] : countries) {
    Poco::JSON::Object item;
    item.set(code, key);
    item.set(en, val);
    ret.set(idx, item);
    idx++;
  }
  ret.stringify(out);
}
